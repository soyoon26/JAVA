package variable;

public class Var5 {
    //변수선언, 초기화 따로
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);
        
        int b = 2; //변수 선언, 초기화 같이
        System.out.println(b);

        int c = 3, d = 4; //변수 선언과 초기화 한번에
        System.out.println(c);
        System.out.println(d);
    }
}
